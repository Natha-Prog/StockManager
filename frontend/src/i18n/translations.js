export function getTranslation(language, key) {
  const keys = key.split('.')
  let value = translations[language] || translations.fr
  for (const k of keys) {
    value = value?.[k]
    if (value === undefined) {
      value = translations.fr
      for (const fk of keys) {
        value = value?.[fk]
      }
      break
    }
  }
  return value ?? key
}
